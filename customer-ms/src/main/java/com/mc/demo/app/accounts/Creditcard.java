package com.mc.demo.app.accounts;

public class Creditcard {

	 private String credicardnumber;
	 private String cardType;
	 private String bankProductcode;
	 private String programID;
	 private String accountStatusID = null;
	 private String pointsTotal;
	 private String houseHoldID;
	/**
	 * @return the credicardnumber
	 */
	public String getCredicardnumber() {
		return credicardnumber;
	}
	/**
	 * @param credicardnumber the credicardnumber to set
	 */
	public void setCredicardnumber(String credicardnumber) {
		this.credicardnumber = credicardnumber;
	}
	/**
	 * @return the cardType
	 */
	public String getCardType() {
		return cardType;
	}
	/**
	 * @param cardType the cardType to set
	 */
	public void setCardType(String cardType) {
		this.cardType = cardType;
	}
	/**
	 * @return the bankProductcode
	 */
	public String getBankProductcode() {
		return bankProductcode;
	}
	/**
	 * @param bankProductcode the bankProductcode to set
	 */
	public void setBankProductcode(String bankProductcode) {
		this.bankProductcode = bankProductcode;
	}
	/**
	 * @return the programID
	 */
	public String getProgramID() {
		return programID;
	}
	/**
	 * @param programID the programID to set
	 */
	public void setProgramID(String programID) {
		this.programID = programID;
	}
	/**
	 * @return the accountStatusID
	 */
	public String getAccountStatusID() {
		return accountStatusID;
	}
	/**
	 * @param accountStatusID the accountStatusID to set
	 */
	public void setAccountStatusID(String accountStatusID) {
		this.accountStatusID = accountStatusID;
	}
	/**
	 * @return the pointsTotal
	 */
	public String getPointsTotal() {
		return pointsTotal;
	}
	/**
	 * @param pointsTotal the pointsTotal to set
	 */
	public void setPointsTotal(String pointsTotal) {
		this.pointsTotal = pointsTotal;
	}
	/**
	 * @return the houseHoldID
	 */
	public String getHouseHoldID() {
		return houseHoldID;
	}
	/**
	 * @param houseHoldID the houseHoldID to set
	 */
	public void setHouseHoldID(String houseHoldID) {
		this.houseHoldID = houseHoldID;
	}
}
